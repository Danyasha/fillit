#include "libft.h"

void	*ft_memchr(const void *memptr, int val, size_t num)
{
	size_t	i;

	i = 0;
	while (i < num)
	{
		if (((char*)memptr)[i] == (char)val)
			return ((void*)&memptr[i]);
		i++;
	}
	return (NULL);
}
